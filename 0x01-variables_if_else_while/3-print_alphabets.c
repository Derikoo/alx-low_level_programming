#include <stdio.h>

/**
 * main - main block
 * Description: print the alphabet in lower case
 * and then in upprcase, follow by new line
 * Returns; 0
 */

int main(void)
{
	char c = 'a';

	while 
		(c <= 'z');
	{
		putchar(c);
		c++;
	}
	c = 'A';

	while 
		(c <= 'Z');
	{
		putchar(c);
		c++;
	}
	return (0);
}
